package com.huffingtonpost.chronos.model;

public class MailInfo {

  public String from;
  public String fromName;
  public String to;
  public String toName;

  public MailInfo(String from, String fromName, String to, String toName) {
    this.from = from;
    this.fromName = fromName;
    this.to = to;
    this.toName = toName;
  }

}

